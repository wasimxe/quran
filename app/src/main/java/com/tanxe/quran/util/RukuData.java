    public static boolean isRukuEnd(int surah, int ayah) {
        return RUKU_ENDS.contains(surah + ":" + ayah);
    }

    /** Global ruku number (1-558). Returns 0 if not a ruku end. */
    public static int getRukuNumber(int surah, int ayah) {
        if (RUKU_NUMBERS == null) buildAllRukuNumbers();
        Integer num = RUKU_NUMBERS.get(surah + ":" + ayah);
        return num != null ? num : 0;
    }

    /** Ruku number within the surah. Returns 0 if not a ruku end. */
    public static int getSurahRukuNumber(int surah, int ayah) {
        if (SURAH_RUKU_NUMBERS == null) buildAllRukuNumbers();
        Integer num = SURAH_RUKU_NUMBERS.get(surah + ":" + ayah);
        return num != null ? num : 0;
    }

    /** Ruku number within the juz. Returns 0 if not a ruku end. */
    public static int getJuzRukuNumber(int surah, int ayah) {
        if (JUZ_RUKU_NUMBERS == null) buildAllRukuNumbers();
        Integer num = JUZ_RUKU_NUMBERS.get(surah + ":" + ayah);
        return num != null ? num : 0;
    }

    /** Get the juz number for a given surah:ayah */
    public static int getJuz(int surah, int ayah) {
        for (int j = JUZ_STARTS.length - 1; j >= 0; j--) {
            int js = JUZ_STARTS[j][0];
            int ja = JUZ_STARTS[j][1];
            if (surah > js || (surah == js && ayah >= ja)) {
                return j + 1;
            }
        }
        return 1;
    }

    private static synchronized void buildAllRukuNumbers() {
        if (RUKU_NUMBERS != null) return;
        RUKU_NUMBERS = new java.util.HashMap<>();
        SURAH_RUKU_NUMBERS = new java.util.HashMap<>();
        JUZ_RUKU_NUMBERS = new java.util.HashMap<>();

        int globalCounter = 0;
        int surahCounter = 0;
        int juzCounter = 0;
        int currentSurah = 1;
        int currentJuz = 1;

        int[] SURAH_AYAH_COUNT = QuranDataParser.SURAH_AYAH_COUNT;
        for (int s = 1; s <= 114; s++) {
            if (s != currentSurah) {
                currentSurah = s;
                surahCounter = 0;
            }
            for (int a = 1; a <= SURAH_AYAH_COUNT[s - 1]; a++) {
                int juz = getJuz(s, a);
                if (juz != currentJuz) {
                    currentJuz = juz;
                    juzCounter = 0;
                }
                String key = s + ":" + a;
                if (RUKU_ENDS.contains(key)) {
                    globalCounter++;
                    surahCounter++;
                    juzCounter++;
                    RUKU_NUMBERS.put(key, globalCounter);
                    SURAH_RUKU_NUMBERS.put(key, surahCounter);
                    JUZ_RUKU_NUMBERS.put(key, juzCounter);
                }
            }
        }
    }
